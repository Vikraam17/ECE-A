var a = 10;
var b = 10;
//Arithematic operator

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

//Relational operator
b = "10";
console.log(a == b);
console.log(a === b);

console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a != b);
console.log(a !== b);

//logical operator
a = true;
b = false;
console.log(a && b);
console.log(a || b);
console.log(!a);

// Increment and Decrement Operator
a = 1;
b = 2;
console.log(a++);
console.log(++a);
console.log(--b);
console.log(b++);

// Shift Operator
a = 4;
console.log(a >> 2);
console.log(a << 2);

// bitwise Operator
a = 0;
b = 1;
console.log(a & b);
console.log(a | b);
console.log(a ^ b);
console.log(~a);
